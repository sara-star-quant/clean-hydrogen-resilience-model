def test_monte_carlo_distribution_shape():
    s = monte_carlo(["WEST_FRIEND", "LATAM_FRIEND"], runs=200, seed=11)
    assert s.runs == 200
    # Non-degenerate: p10 <= p50 <= p90, std > 0.
    assert s.p10 <= s.p50 <= s.p90
    assert s.std > 0.0


def test_friends_union_helps_under_shock():
    """Three well-matched blocs > no blocs at the same seed (median).

    The 'helped' posture is chosen for shock-coverage breadth:
    EAST_FRIEND covers china_decoupling/maritime/li/pt; AFRICA_FRIEND covers
    ir/pt; SOUTH_CENTRAL_FRIEND covers li/maritime/regional_autarky. With at
    least three blocs, the Friends Union umbrella (0.75) also kicks in.
    """
    empty = monte_carlo([], runs=200, seed=42)
    helped = monte_carlo(
        ["EAST_FRIEND", "AFRICA_FRIEND", "SOUTH_CENTRAL_FRIEND"], runs=200, seed=42
    )
    assert helped.p50 > empty.p50


def test_friction_pair_hurts():
    """WEST+EAST friction reduces median below just WEST under default mix."""
    just_west = monte_carlo(["WEST_FRIEND"], runs=300, seed=99)
    west_east = monte_carlo(["WEST_FRIEND", "EAST_FRIEND"], runs=300, seed=99)
    assert west_east.p50 < just_west.p50



_posture_strategy = st.sets(st.sampled_from(BLOC_NAMES), max_size=4).map(frozenset)
_shock_strategy = st.sampled_from(SHOCK_NAMES)


@given(posture=_posture_strategy, shock=_shock_strategy)
@settings(max_examples=80, deadline=None)
def test_property_combined_damping_in_unit_interval(
    posture: frozenset[str], shock: str,
) -> None:
    if sovereignty_spent(posture) > SOVEREIGNTY_MAX:
        return
    d = combined_damping(shock, posture)
    assert 0.0 <= d <= 1.0


@given(posture=_posture_strategy)
@settings(max_examples=50, deadline=None)
def test_property_shock_distribution_normalized_and_clamped(
    posture: frozenset[str],
) -> None:
    d = shock_distribution(posture)
    assert sum(d.values()) == pytest.approx(1.0, abs=1e-9)
    for v in d.values():
        assert -1e-12 <= v <= 0.5 + 1e-9


@given(seed=st.integers(min_value=0, max_value=10_000))
@settings(max_examples=20, deadline=None)
def test_property_simulate_campaign_deterministic(seed: int) -> None:
    posture = ["WEST_FRIEND", "LATAM_FRIEND"]
    a = simulate_campaign(posture, seed=seed, years=3)
    b = simulate_campaign(posture, seed=seed, years=3)
    assert a.total_score == b.total_score
    assert tuple(yr.shock for yr in a.years) == tuple(yr.shock for yr in b.years)



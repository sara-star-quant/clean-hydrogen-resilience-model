import json
import shutil
import subprocess
from pathlib import Path


_PARITY_HARNESS = Path(__file__).parent / "_js_parity_harness.js"
_NODE_BIN = shutil.which("node")


@pytest.mark.skipif(_NODE_BIN is None, reason="node not available; behavioral parity test skipped")
def test_js_python_behavioral_parity(tmp_path):
    """JS playground and Python diplomacy module produce identical numerics
    for a fixture of math-kernel calls: Mulberry32 sequences, combined_damping,
    and shock_distribution. Tolerance 1e-9 (these are deterministic floats)."""
    fixtures = {
        "mulberry32": [
            {"seed": 42, "n": 8},
            {"seed": 1, "n": 4},
            {"seed": 0, "n": 4},
        ],
        "combined_damping": [
            {"shock": "bau", "posture": [], "trust": None},
            {"shock": "china_decoupling", "posture": ["EAST_FRIEND"], "trust": None},
            {"shock": "pt_shortage", "posture": ["AFRICA_FRIEND", "NORTH_FRIEND"], "trust": None},
            {"shock": "li_shortage", "posture": ["LATAM_FRIEND", "SOUTH_CENTRAL_FRIEND"], "trust": None},
            {"shock": "maritime_blockade", "posture": ["EAST_FRIEND", "SOUTH_CENTRAL_FRIEND", "AFRICA_FRIEND"], "trust": None},
        ],
        "shock_distribution": [
            {"posture": []},
            {"posture": ["WEST_FRIEND"]},
            {"posture": ["EAST_FRIEND", "AFRICA_FRIEND"]},
            {"posture": ["EU_FRIEND", "LATAM_FRIEND", "AFRICA_FRIEND"]},
        ],
    }
    fixture_path = tmp_path / "fixtures.json"
    fixture_path.write_text(json.dumps(fixtures))
    proc = subprocess.run(
        [str(_NODE_BIN), str(_PARITY_HARNESS), str(fixture_path)],
        capture_output=True,
        text=True,
        timeout=30,
    )
    assert proc.returncode == 0, f"harness failed: {proc.stderr}"
    js_out = json.loads(proc.stdout)

    # 1. Mulberry32 sequence parity
    for fx, js_seq in zip(fixtures["mulberry32"], js_out["mulberry32"]):
        rng = Mulberry32(fx["seed"])
        py_seq = [rng.next_float() for _ in range(fx["n"])]
        assert len(py_seq) == len(js_seq)
        for j, p in zip(js_seq, py_seq):
            assert abs(j - p) < 1e-12, f"Mulberry32 seed={fx['seed']} mismatch: js={j} py={p}"

    # 2. combined_damping parity
    for fx, js_val in zip(fixtures["combined_damping"], js_out["combined_damping"]):
        py_val = combined_damping(fx["shock"], frozenset(fx["posture"]))
        assert abs(js_val - py_val) < 1e-9, (
            f"combined_damping({fx['shock']!r}, {fx['posture']!r}) "
            f"mismatch: js={js_val} py={py_val}"
        )

    # 3. shock_distribution parity
    for fx, js_dist in zip(fixtures["shock_distribution"], js_out["shock_distribution"]):
        py_dist = shock_distribution(frozenset(fx["posture"]))
        assert set(js_dist) == set(py_dist), "shock keys differ"
        for k in py_dist:
            assert abs(js_dist[k] - py_dist[k]) < 1e-9, (
                f"shock_distribution({fx['posture']!r})[{k}] "
                f"mismatch: js={js_dist[k]} py={py_dist[k]}"
            )

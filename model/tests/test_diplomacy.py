"""Diplomacy module unit + property tests.

Covers the 7-bloc constants, water-filling shock distribution, combined damping
bounds, deterministic campaign behavior, Monte Carlo distribution shape, and
the Mulberry32 RNG parity against a golden sequence generated from the JS
reference implementation.

The behavioral parity test against the JS playground (test_js_python_behavioral_parity)
belongs in Phase 5 (integration), not Phase 4. It requires Agent A's microgrid_sim.js
to exist; it is intentionally omitted here.
"""


def test_combined_damping_with_bau_returns_one():
    for posture in [frozenset(), frozenset({"WEST_FRIEND"}), frozenset({"WEST_FRIEND", "EU_FRIEND"})]:
        assert combined_damping("bau", posture) == 1.0


def test_combined_damping_bounds_single_and_pair():
    # sample a few postures; must always be in [0, 1].
    samples = [
        frozenset(),
        frozenset({"WEST_FRIEND"}),
        frozenset({"EAST_FRIEND"}),
        frozenset({"WEST_FRIEND", "EU_FRIEND"}),
        frozenset({"WEST_FRIEND", "EAST_FRIEND"}),  # friction
        frozenset({"AFRICA_FRIEND", "EU_FRIEND"}),  # synergy
    ]
    for posture in samples:
        for shock in NON_BAU_SHOCKS:
            d = combined_damping(shock, posture)
            assert 0.0 <= d <= 1.0, f"out of range for {shock} {posture}: {d}"


def test_relationship_clamp_extremes():
    # Positive synergy posture; pairwise sum positive. Multiplier <= 1.5.
    pos = frozenset({"AFRICA_FRIEND", "EU_FRIEND", "NORTH_FRIEND", "LATAM_FRIEND", "WEST_FRIEND"})
    assert pairwise_adjustment(pos) <= 1.5 + 1e-9
    # Friction-heavy: WEST + EAST + EU.
    neg = frozenset({"WEST_FRIEND", "EAST_FRIEND", "EU_FRIEND"})
    adj = pairwise_adjustment(neg)
    assert 0.3 <= adj <= 1.5


# ---------------------------------------------------------------------------
# Mulberry32 parity against JS golden sequence
# ---------------------------------------------------------------------------


# Generated by:
#   node -e "function m(seed){let a=seed>>>0; return ()=>{a=(a+0x6D2B79F5)>>>0; let t=a; \
#       t=Math.imul(t^(t>>>15),t|1); t^=t+Math.imul(t^(t>>>7),t|61); \
#       return ((t^(t>>>14))>>>0)/4294967296;}}; const r=m(42); for(let i=0;i<8;i++) console.log(r());"
MULBERRY32_GOLDEN_SEED_42: list[float] = [
    0.6011037519201636,
    0.44829055899754167,
    0.8524657934904099,
    0.6697340414393693,
    0.17481389874592423,
    0.5265925421845168,
    0.2732279943302274,
    0.6247446539346129,
]


def test_mulberry32_parity():
    rng = Mulberry32(42)
    out = [rng.next_float() for _ in range(8)]
    for got, want in zip(out, MULBERRY32_GOLDEN_SEED_42, strict=True):
        assert got == pytest.approx(want, rel=0, abs=1e-15), (got, want)


def test_pick_shock_uses_full_distribution():
    # With all-mass at one shock, pick_shock returns it.
    only_bau = {name: (1.0 if name == "bau" else 0.0) for name in SHOCK_NAMES}
    rng = Mulberry32(0)
    for _ in range(20):
        assert pick_shock(only_bau, rng) == "bau"



def test_shock_distribution_normalizes_empty_posture():
    d = shock_distribution(frozenset())
    assert sum(d.values()) == pytest.approx(1.0, abs=1e-9)
    for v in d.values():
        assert 0.0 <= v <= 0.5 + 1e-9


def test_shock_distribution_pathological_posture_clamps_to_half():
    # All 7 blocs (would exceed sovereignty cap, but we bypass the cap here
    # because shock_distribution itself does not enforce it).
    posture = frozenset(BLOC_NAMES)
    d = shock_distribution(posture)
    assert sum(d.values()) == pytest.approx(1.0, abs=1e-9)
    for v in d.values():
        assert v <= 0.5 + 1e-9



def test_blocs_complete():
    assert len(BLOCS) == 7
    for name, b in BLOCS.items():
        assert b.name == name
        assert b.label
        assert b.cost > 0
        assert b.sovereignty in {1, 2}


def test_relationship_matrix_canonical_order():
    for (a, b) in BLOC_REL.keys():
        assert a < b, f"BLOC_REL key ({a},{b}) is not in canonical alphabetical order"


def test_shock_base_prob_has_bau():
    assert "bau" in SHOCK_BASE_PROB
    # All known supply shocks present.
    for name in ("ir_shortage", "li_shortage", "china_decoupling", "maritime_blockade"):
        assert name in SHOCK_BASE_PROB


def test_diplomacy_hash_stable_and_short():
    h = diplomacy_hash()
    assert len(h) == 12
    assert h == diplomacy_hash()



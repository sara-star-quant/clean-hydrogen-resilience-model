def test_sovereignty_overflow_raises():
    # All 7 = 1+1+2+2+1+2+1 = 10, at the cap.
    full = list(BLOC_NAMES)
    spent = sovereignty_spent(frozenset(full))
    assert spent == SOVEREIGNTY_MAX
    # Build a synthetic over-cap posture by listing one bloc twice. The frozenset
    # collapses duplicates, so instead: pick a posture whose sovereignty exceeds 10.
    # Since sum of all blocs is exactly 10, we cannot construct an over-cap posture
    # from the 7 valid blocs alone. Verify the cap is at-the-limit acceptable:
    _ = simulate_campaign(full, seed=0, years=1)


def test_bloc_bonus_applies_under_bau():
    """A WEST_FRIEND posture (PTC rebate) should produce strictly lower year-1 capex
    than an empty posture under bau, because the rebate exceeds the bloc fee."""
    # Year 1 only, run a 1-year campaign with seed that draws bau (drawn from dist).
    # Easier path: directly compare monte_carlo means under all-bau-weighted draw.
    # Just compare simulated single year via simulate_campaign over many seeds.
    west_capex_total = 0.0
    empty_capex_total = 0.0
    n = 30
    for s in range(n):
        rw = simulate_campaign(["WEST_FRIEND"], seed=s, years=1)
        re = simulate_campaign([], seed=s, years=1)
        # Only count years where shock == bau in BOTH (so we compare apples to apples).
        if rw.years[0].shock == "bau" and re.years[0].shock == "bau":
            west_capex_total += rw.years[0].capex_total
            empty_capex_total += re.years[0].capex_total
    # If we got at least a few matched bau years, WEST should be strictly cheaper.
    assert west_capex_total > 0.0  # at least some matched
    assert west_capex_total < empty_capex_total


